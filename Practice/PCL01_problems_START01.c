/*Number Mirror
Write a program that takes a number 
N as the input, and prints it to the output.

Input Format
The only line of input contains a single integer.

Output Format
Output the answer in a single line.*/
#include <stdio.h>

int main(void) {
	int N;
    
    // Taking input from the user
    
    scanf("%d", &N);
    
    // Printing the number
    printf("%d\n", N);
    
    return 0;

}